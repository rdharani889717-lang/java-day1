class Datatype
{
    public static void main(String args[])
    {
        byte a=124;
        short b=136;
        int c=150000;
        long d=100000000;
        float e=1.2f;
        double g=120.555;
        char ch='d';
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
        System.out.println(g);
        System.out.println(ch);
    }
}